package com.sky.Bean;

public class TextShout {
	private int textShout_id;
	private User user;
	private String textContent;
	
	public int getTextShout_id() {
		return textShout_id;
	}
	public void setTextShout_id(int textShout_id) {
		this.textShout_id = textShout_id;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	public String getTextContent() {
		return textContent;
	}
	public void setTextContent(String textContent) {
		this.textContent = textContent;
	}
	
}
